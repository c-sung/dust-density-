package tw.saihs.testserver.api;

public class DetectData {
    public String dd;
    public String temp;
    public String humid;

    public void setDd(String dd) {
        this.dd = dd;
    }

    public String getDd() {
        return this.dd;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getTemp() {
        return this.temp;
    }

    public void setHumid(String humid) {
        this.humid = humid;
    }

    public String getHumid() {
        return this.humid;
    }

}
